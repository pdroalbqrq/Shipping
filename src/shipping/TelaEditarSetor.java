    private void jButtonEditarSetorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarSetorActionPerformed
        try {
            if (jTextFieldEditarSetor.getText().equals("Novo nome setor")) {
                jTextFieldEditarSetor.setText(null);
            }
            if (jComboBoxSelecionarSetor.getSelectedIndex() < 0) {
                JOptionPane.showMessageDialog(rootPane, "Favor selecionar o setor ");
                return;
            }
            Setor s = this.listaSetor.get(jComboBoxSelecionarSetor.getSelectedIndex());
            s.setNomeSetor(jTextFieldEditarSetor.getText());
            SetorNegocio sN = new SetorNegocio();
            sN.alterarSetor(s);
            
            DefaultComboBoxModel modelo = new DefaultComboBoxModel();
            SetorDados dadosS = new SetorDados();
            listaSetor = dadosS.listarSetor();
            for (Setor s1 : listaSetor) {
                modelo.addElement(s1.getNomeSetor());
            }
            jComboBoxSelecionarSetor.setModel(modelo);
            jComboBoxSelecionarSetor.setSelectedIndex(-1);
            jTextFieldEditarSetor.setText("");
            jComboBoxSelecionarSetor.setSelectedIndex(-1);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }

    }//GEN-LAST:event_jButtonEditarSetorActionPerformed

    private void jTextFieldEditarSetorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldEditarSetorFocusLost
        if (jTextFieldEditarSetor.getText().equals("")) {
            jTextFieldEditarSetor.setFont(new Font("Tahoma", Font.ITALIC, 10));
            jTextFieldEditarSetor.setText("Novo nome setor");
        }
        if (jTextFieldEditarSetor.getText() == null) {
            jTextFieldEditarSetor.setFont(new Font("Tahoma", Font.ITALIC, 10));
            jTextFieldEditarSetor.setText("Novo nome setor");
        }

    }//GEN-LAST:event_jTextFieldEditarSetorFocusLost

